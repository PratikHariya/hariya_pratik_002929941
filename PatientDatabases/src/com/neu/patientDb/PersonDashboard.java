    private void populateCbId(Map<Integer, Person> personMap) {
        
        Iterator it = personMap.entrySet().iterator();
        cbId.removeAllItems();
        while(it.hasNext()) {
            
            Map.Entry mapEl = (Map.Entry)it.next();           
            cbId.addItem(String.valueOf(mapEl.getKey()));
        }
    }    

