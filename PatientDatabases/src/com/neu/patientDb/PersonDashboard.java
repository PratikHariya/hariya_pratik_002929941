    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        if(txtId.getText().equalsIgnoreCase("") || txtName.getText().equalsIgnoreCase("") ||
                txtAge.getText().equalsIgnoreCase("") || txtGender.getText().equalsIgnoreCase("") ||
                txtPhoneNo.getText().equalsIgnoreCase("") || txtHouseNo.getText().equalsIgnoreCase("") ||
                txtCommunity.getText().equalsIgnoreCase("") || txtCity.getText().equalsIgnoreCase("")) {
            
            JOptionPane.showMessageDialog(this, "No fields can be empty");
            return;
        }
        
        try {
            int id = Integer.parseInt(txtId.getText());
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "ID must be integer");
            return;
        }
        
        try {
            int age = Integer.parseInt(txtAge.getText());
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Age must be integer");
            return;
        } 
        
        try {
            long no = Long.parseLong(txtPhoneNo.getText());
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Phone number must be interger");
            return;
        }
        
        if(txtPhoneNo.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "Phone number must be 10 digits long");
            return;            
        }
        
        if(!txtGender.getText().equalsIgnoreCase("male") && !txtGender.getText().equalsIgnoreCase("female")) {
            
            JOptionPane.showMessageDialog(this, "Gender must be male or female");
            return;            
        }
        
        Person person = new Person();

        person.setId(Integer.parseInt(txtId.getText()));
        person.setName(txtName.getText());
        person.setAge(Integer.parseInt(txtAge.getText()));
        person.setPhoneNumber(Long.parseLong(txtPhoneNo.getText()));
        person.setGender(txtGender.getText());
        person.setHouseNum(txtHouseNo.getText());
        person.setCommunityName(txtCommunity.getText());
        person.setCityName(txtCity.getText());
        
        System.out.println("age Group"+person.getAgeGroup());
        personDirectorty.getPersonMap().put(person.getId(), person);
        
        cbId.addItem(String.valueOf(person.getId()));
        System.out.println(personDirectorty.getPersonMap());
        
        //person.setPatient((Patient)person);
        JOptionPane.showMessageDialog(this, "Person Created");
        
        txtId.setText("");
        txtName.setText("");
        txtAge.setText("");
        txtGender.setText("");
        txtPhoneNo.setText("");
        txtHouseNo.setText("");
        txtCommunity.setText("");
        txtCity.setText("");
        
    }//GEN-LAST:event_btnSaveActionPerformed

